# CÓDIGOS DE CORES EM PYTHON

print('\033[31mOlá, mundo!!')  #letras em vermelho
print('\033[31;43mOlá, mundo!!') #letras em vermelho, fundo amarelo
print('\033[1;31mOlá, mundo!!') #letras em vermelho negrito, fundo amarelo
print('\033[1;31mOlá, mundo!!\033[m') #fundo amarelo só na mensagem
print('\033[4;30;45mOlá, mundo!!\033[m') #letra preta, sublinhada, com fundo lilas
print('\033[39mOlá, mundo!!\033[m') #letra branca
print('\033[7;39mOlá, mundo!!\033[m')#letra preta, fundo branco

